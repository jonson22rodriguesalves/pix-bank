package model;

/**
 * Enumeração que representa os tipos de serviços bancários disponíveis no sistema.
 * Define as categorias principais de operações financeiras suportadas.
 */
public enum BankService {

    /**
     * Serviço relacionado a operações bancárias tradicionais em contas correntes.
     * Inclui depósitos, saques e transferências.
     */
    ACCOUNT,

    /**
     * Serviço relacionado a operações de investimentos financeiros.
     * Inclui aplicações, resgates e rendimentos.
     */
    INVESTMENT

}